const answer = document.getElementById("answer").value;
//--------------------------------------------------
let correctAnswer = 0;
//------------------
let falseAnswer = 0;
//------------------
let emptyAnswer = 0;
//------------------
document.getElementById('submit').onclick = function () {
    //-----------------------------------------------
    var value = document.getElementsByName('option');
    //-----------------------------------------------
    for (var radio of value) {
        if (radio.checked) {
            //alert(radio.value)
            if (radio.value == answer) {
                //alert("correct");
                correctAnswer++;
            } else {
                falseAnswer++;
                //alert("false");
            }
        } else {
            emptyAnswer++;
            //alert("empty");
        }
    }
};








